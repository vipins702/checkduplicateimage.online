    function calculateLove() {
        const name1 = document.getElementById('name1').value.trim().toLowerCase();
        const name2 = document.getElementById('name2').value.trim().toLowerCase();

        if (!name1 || !name2) {
            alert('Please enter both names.');
            return;
        }

        const lovePercentage = generateLovePercentage(name1, name2);

        // Display circular progress bar
        const progressElement = document.getElementById('progress');
        progressElement.classList.remove('hidden');

        // Update circular progress bar based on lovePercentage
        updateProgressBar(lovePercentage);

        // Display the result after a delay (for demo purposes)
        setTimeout(() => {
            const resultElement = document.getElementById('result');
            resultElement.innerHTML = `<p>The love percentage between ${capitalize(name1)} and ${capitalize(name2)} is ${lovePercentage}%.</p>`;
        }, 1000); // Adjust the delay as needed
    }

    function generateLovePercentage(name1, name2) {
        const combinedNames = name1 + name2;
        let hash = 0;

        for (let i = 0; i < combinedNames.length; i++) {
            hash += combinedNames.charCodeAt(i);
        }

        return hash % 101; // Love percentage is between 0 and 100
    }

    function capitalize(str) {
        return str.charAt(0).toUpperCase() + str.slice(1);
    }

    function updateProgressBar(percentage) {
        const circle = document.getElementById('progress-circle');
        const percent = document.getElementById('percent');

        // Calculate the rotation angle based on the percentage
        const rotation = (percentage / 100) * 360;

        // Update the progress circle
        circle.style.transform = `rotate(${rotation}deg)`;

        // Update the percentage text
        percent.textContent = `${percentage}%`;
    }
    
    
     function calculateFLAMES() {
            var partner1 = document.getElementById("partner1").value.toLowerCase().replace(/\s/g, '');
            var partner2 = document.getElementById("partner2").value.toLowerCase().replace(/\s/g, '');

            var flames = ['Friendship', 'Love', 'Affection', 'Marriage', 'Enemy', 'Sibling'];
            var combinedNames = partner1 + partner2;
            var totalLetters = combinedNames.length;

            // Simple FLAMES algorithm
            while (flames.length > 1) {
                var index = totalLetters % flames.length;
                flames.splice(index, 1);
            }

            // Display the result
            var resultElement = document.getElementById("result1");
            resultElement.innerHTML = "FLAMES Result: " + flames[0];
        }
