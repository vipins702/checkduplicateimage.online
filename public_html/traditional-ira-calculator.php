            if (isNaN(startingBalance) || isNaN(annualContributionInput) || isNaN(currentAge) || isNaN(retirementAge) || isNaN(adjustedGrossIncome) || isNaN(expectedRateOfReturn) || isNaN(currentTaxRate) || isNaN(retirementTaxRate)) {
