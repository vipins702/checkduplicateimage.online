  function calculateTotalIRABalance() {
            // Get user inputs
            const startingBalance = parseFloat(document.getElementById('startingBalance').value) || 0;
            const annualContributionInput = parseFloat(document.getElementById('annualContribution').value) || 0;
            const currentAge = parseInt(document.getElementById('currentAge').value) || 0;
            const retirementAge = parseInt(document.getElementById('retirementAge').value) || 0;
            const adjustedGrossIncome = parseFloat(document.getElementById('adjustedGrossIncome').value) || 0;
            const expectedRateOfReturn = parseFloat(document.getElementById('expectedRateOfReturn').value) || 0;
            const maximizeContributions = document.getElementById('maximizeContributions').checked;
            

            // Validate input values
            if (isNaN(startingBalance) || isNaN(annualContributionInput) || isNaN(currentAge) || isNaN(retirementAge) || isNaN(adjustedGrossIncome) || isNaN(expectedRateOfReturn)) {
                alert('Please enter valid numeric values for all input fields.');
                return;
            }

            // Adjust contributions based on the checkbox
            const annualContribution = maximizeContributions
                ? Math.min(adjustedGrossIncome * 0.15, 6000)  // Adjust the maximum contribution based on IRS rules
                : annualContributionInput;

            // Example calculation:
            const yearsToRetirement = retirementAge - currentAge;
            const totalContributions = annualContribution * yearsToRetirement;

            // Call a separate function to calculate the total IRA balance
            const totalIRABalance = calculateIRABalance(startingBalance, annualContribution, currentAge, retirementAge, expectedRateOfReturn);

            // Display results
            const resultMessage = `Total Contributions: $${totalContributions.toFixed(2)}<br>Total IRA Balance: $${totalIRABalance.toFixed(2)}`;
            document.getElementById('result1').innerHTML = resultMessage;
        }

        function calculateIRABalance(startingBalance, annualContribution, currentAge, retirementAge, expectedRateOfReturn) {
            let totalIRABalance = startingBalance;
            let yearlyReturn = 0;

            // Loop until current age is greater than or equal to retirement age
            while (currentAge < retirementAge) {
                // Calculate yearly return based on the current total IRA balance and annual contribution
                yearlyReturn = (totalIRABalance + annualContribution) * (expectedRateOfReturn / 100);

                // Update total IRA balance for the current year
                totalIRABalance = totalIRABalance + yearlyReturn + annualContribution;

                // Increment current age
                currentAge++;
            }

            // Return the total IRA balance
            return totalIRABalance;
        }

