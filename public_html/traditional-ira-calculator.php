        // Separate function to calculate adjusted contribution based on the checkbox
        function calculateAdjustedContribution(adjustedGrossIncome, annualContributionInput) {
            const maximizeContributions = document.getElementById('maximizeContributions').checked;

            return maximizeContributions
                ? Math.min(adjustedGrossIncome * 0.15, 6000)  // Adjust the maximum contribution based on IRS rules
                : annualContributionInput;
        }

        // Separate function to calculate the total IRA balance with tax details
        function calculateIRAAfterTax(
            startingBalance,
            annualContribution,
            currentAge,
            retirementAge,
            expectedRateOfReturn,
            currentTaxRate,
            retirementTaxRate,
            isMarried,
            hasEmployerPlan
        ) {
            let totalIRABalance = startingBalance;
            let yearlyReturn = 0;

            // Loop until current age is greater than or equal to retirement age
            while (currentAge < retirementAge) {
                // Calculate yearly return based on the current total IRA balance and annual contribution
                yearlyReturn = (totalIRABalance + annualContribution) * (expectedRateOfReturn / 100);

                // Adjust contribution based on marital status and employer plan
                if (isMarried && hasEmployerPlan) {
                    annualContribution = annualContribution * 0.9; // Example adjustment for married with an employer plan
                }

                // Adjust contribution based on tax rates
                if (currentAge < retirementAge) {
                    annualContribution = annualContribution * (1 - currentTaxRate); // Adjust for current tax rate
                } else {
                    annualContribution = annualContribution * (1 - retirementTaxRate); // Adjust for retirement tax rate
                }

                // Update total IRA balance for the current year
                totalIRABalance = totalIRABalance + yearlyReturn + annualContribution;

                // Increment current age
                currentAge++;
            }

            // Return the total IRA balance with tax details
            return totalIRABalance;
