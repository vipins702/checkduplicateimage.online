<script>
    function calculateSavings() {
        // Get input values
        var startingBalance = parseFloat(document.getElementById('startingBalance').value) || 0;
        var contributionAmount = parseFloat(document.getElementById('contributionAmount').value) || 0;
        var contributionFrequency = document.getElementById('contributionFrequency').value;
        var timeToGrow = parseInt(document.getElementById('timeToGrow').value) || 0;
        var annualInterestRate = parseFloat(document.getElementById('annualInterestRate').value) || 0;
        var compoundFrequency = document.getElementById('compoundFrequency').value;

        // Calculate future value
        var periods = timeToGrow * getPeriodsPerYear(contributionFrequency);
        var interestRatePerPeriod = annualInterestRate / getPeriodsPerYear(compoundFrequency) / 100;

        var futureValue = startingBalance * Math.pow((1 + interestRatePerPeriod), periods) +
            contributionAmount * ((Math.pow((1 + interestRatePerPeriod), periods) - 1) / interestRatePerPeriod);

        // Calculate Total Contribution
        var totalContribution = contributionAmount * getFrequencyMultiplier(contributionFrequency) * timeToGrow;

        // Format the result with commas for thousands, millions, and billions
        var formattedResult = formatResult(futureValue);

        // Display result
        var resultElement = document.getElementById('result');
        resultElement.innerHTML = '<p><strong>Starting Balance:</strong> ' + formatResult(startingBalance) + '</p>' +
            '<p><strong>Total Contribution:</strong> ' + formatResult(totalContribution) + '</p>' +
            '<p><strong>Interest Earned:</strong> ' + formatResult(futureValue - startingBalance - totalContribution) + '</p>' +
            '<p><strong>Future Value:</strong> <span style="color: #4da859; font-size: 48px;">' + formattedResult + '</span></p>';
    }

    function getPeriodsPerYear(frequency) {
        switch (frequency) {
            case 'daily':
                return 365;
            case 'monthly':
                return 12;
            case 'quarterly':
                return 4;
            case 'halfyearly':
                return 2;
            case 'annually':
                return 1;
            default:
                return 1;
        }
    }

    function getFrequencyMultiplier(frequency) {
        switch (frequency) {
            case 'daily':
                return 365;
            case 'monthly':
                return 12;
            case 'quarterly':
                return 4;
            case 'halfyearly':
                return 2;
            case 'annually':
                return 1;
            default:
                return 1;
        }
    }

    function formatResult(value) {
        // Check if the value is negative
        var isNegative = value < 0;
        value = Math.abs(value);

        return (isNegative ? '-' : '') + '$' + value.toFixed(2).replace(/\B(?=(\d{3})+(?!\d))/g, ',');
    }
</script>



 

   
  

   
