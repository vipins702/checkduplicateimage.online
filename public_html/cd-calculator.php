    function calculateCD() {
        var depositAmount = parseFloat(document.getElementById("depositAmount").value);
        var term = document.getElementById("term").value;
        var termValue = parseFloat(document.getElementById("termInput").value);
        var apy = parseFloat(document.getElementById("apy").value);

        var resultLabel = document.getElementById("result").querySelector("strong");
        var chartData = {
            x: 'x',
            columns: [['x'], ['Initial Value'], ['Interest Earned'], ['Total Value']],
            type: 'pie',
            groups: [['Initial Value', 'Interest Earned', 'Total Value']],
        };

        var totalValue = depositAmount;

        for (var i = 1; i <= termValue; i++) {
            var interestRate = apy / 100 / (term === 'monthly' ? 12 : 1);
            var interestEarned = totalValue * interestRate;
            totalValue = totalValue + interestEarned;
            
            chartData.columns[0].push(i); // Use numerical values for x-axis
            chartData.columns[1].push(depositAmount);
            chartData.columns[2].push(interestEarned);
            chartData.columns[3].push(totalValue);
        }

        var chart = c3.generate({
            bindto: '#chart',
            data: chartData,
            axis: {
                x: {
                    label: 'Year/Month',
                    tick: {
                        fit: false
                    }
                },
                y: {
                    label: {
                        text: 'CD Value ($)',
                        position: 'outer-middle'
                    }
                }
            },
            color: {
                pattern: ['#1f77b4', '#ff7f0e', '#2ca02c']
            },
            legend: {
                position: 'right'
            },
            point: {
                r: 50, // Radius of the circles
            },
            tooltip: {
                format: {
                    title: function (d) {
                        return term === 'monthly' ? 'Month ' + d : 'Year ' + d;
                    },
                    value: function (value, ratio, id) {
                        return value.toFixed(2);
                    }
                }
            }
        });

        var tableBody = document.getElementById("tableBody");
        tableBody.innerHTML = '';

        totalValue = depositAmount;  // Reset totalValue for table calculation

        for (var i = 1; i <= termValue; i++) {
            var interestRate = apy / (term === 'monthly' ? 12 : 1);
            var interestEarned = totalValue * (interestRate / 100);
            totalValue = totalValue + interestEarned;

            var row = document.createElement("tr");
            row.innerHTML = "<td>" + (term === 'monthly' ? 'Month ' : 'Year ') + i + "</td><td>" + depositAmount.toFixed(2) + "</td><td>" + interestRate.toFixed(2) + "</td><td>" + interestEarned.toFixed(2) + "</td><td>" + totalValue.toFixed(2) + "</td>";
            tableBody.appendChild(row);
        }

        // Display result in strong tag with green color
        resultLabel.innerHTML = 'Total Value: <strong style="color: white;">' + totalValue.toFixed(2) + '</strong>';
    }
