  function calculateDifference() {
        const dobInput = document.getElementById('dob');
        const asOfDateInput = document.getElementById('asOfDate');
        const includeTodayCheckbox = document.getElementById('includeToday');
        const dob = new Date(dobInput.value);
        const asOfDate = asOfDateInput.value ? new Date(asOfDateInput.value) : new Date();

       

        // Scenario 2: If "Include Today's Date" is checked, include today's date in the calculation
        if (includeTodayCheckbox.checked) {
            asOfDate.setHours(0, 0, 0, 0);
        }
        
   

        // Validate if the selected date of birth is in the future
        if (dob.getTime() > asOfDate.getTime()) {
            alert('Please select a valid DOB (Not Future Date) or As of Date (Not Less than DOB).');
            return;
        }

        const ageInMilliseconds = asOfDate - dob;

        const years = Math.floor(ageInMilliseconds / (365.25 * 24 * 60 * 60 * 1000));
        const months = Math.floor((ageInMilliseconds % (365.25 * 24 * 60 * 60 * 1000)) / (30.44 * 24 * 60 * 60 * 1000));
        const days = Math.floor((ageInMilliseconds % (30.44 * 24 * 60 * 60 * 1000)) / (24 * 60 * 60 * 1000));

        const ageResultElement = document.getElementById('ageResult');
        ageResultElement.style.background = '#e0f7fa';
        const ageResultContainer = document.createElement('div');
        ageResultContainer.style.fontSize = '18px';
        ageResultContainer.style.marginTop = '15px';
        ageResultContainer.style.color = '#333';

        const ageResultText = document.createTextNode(`Your age is: ${years} years, ${months} months, ${days} days.`);
        ageResultContainer.appendChild(ageResultText);

        ageResultElement.innerHTML = ''; // Clear previous results
        ageResultElement.appendChild(ageResultContainer);

        // Scenario 3: If the second text box is selected, provide the difference between the first and second text box dates
        if (asOfDateInput.value) {
            const diffInMilliseconds = asOfDate - new Date(asOfDateInput.value);

            const diffYears = Math.floor(diffInMilliseconds / (365.25 * 24 * 60 * 60 * 1000));
            const diffMonths = Math.floor((diffInMilliseconds % (365.25 * 24 * 60 * 60 * 1000)) / (30.44 * 24 * 60 * 60 * 1000));
            const diffDays = Math.floor((diffInMilliseconds % (30.44 * 24 * 60 * 60 * 1000)) / (24 * 60 * 60 * 1000));

            const diffResultElement = document.getElementById('diffResult');
            diffResultElement.style.background = '#ffebee';
            const diffResultContainer = document.createElement('div');
            diffResultContainer.style.fontSize = '18px';
            diffResultContainer.style.marginTop = '15px';
            diffResultContainer.style.color = '#333';

            const diffResultText = document.createTextNode(`Difference: ${diffYears} years, ${diffMonths} months, ${diffDays} days.`);
            diffResultContainer.appendChild(diffResultText);

            diffResultElement.innerHTML = ''; // Clear previous results
            diffResultElement.appendChild(diffResultContainer);
        }
    }

    function clearForm() {
        document.getElementById('dob').value = '';
        document.getElementById('asOfDate').value = '';
        document.getElementById('includeToday').checked = false;
        document.getElementById('ageResult').innerHTML = '';
        document.getElementById('diffResult').innerHTML = '';
    }
