<script>
    let currentQuestion = 1;
    let totalScore = 0;

    const answerMetadata = {
        '5': 'Almost Always or Always',
        '4': 'Most Usually',
        '3': 'Usually Sometimes',
        '2': 'Somewhat Rarely',
        '1': 'Never Have'
    };

    function nextQuestion(answerValue) {
        totalScore += answerValue;

        document.getElementById(`question${currentQuestion}`).style.display = 'none';
        currentQuestion++;

        if (currentQuestion <= 13) {
            document.getElementById(`question${currentQuestion}`).style.display = 'block';
        } else {
            showResult();
        }
    }

    function showResult() {
        let resultText = '<h2>Result</h2>';
        resultText += '<ul>';

        for (let i = 1; i <= 13; i++) {
            const selectedOption = document.querySelector(`#question${i} input:checked`);
            if (selectedOption) {
                const answerValue = parseInt(selectedOption.value, 10);
                const questionScore = `<li>Question ${i}: ${answerMetadata[answerValue.toString()]} (Score: ${answerValue})</li>`;
                resultText += questionScore;
            }
        }

         resultText += `<li><strong>Total Score:</strong> ${totalScore} Out of 65</li>`;
            resultText += '</ul>';

            // Display additional result based on totalScore
            resultText += '<h2>Scoring Results:</h2>';
            if (totalScore > 55) {
                resultText += 'Above 55: Be pleased that you are an extremely supportive person to yourself and anyone you come in contact with - you\'re someone whose optimism is a powerful healing force, possibly mentally and physically.';
            } else if (totalScore >= 50) {
                resultText += '50 - 55: This is still Excellent. You should be happy and proud that you are a genuine positive thinker.';
            } else if (totalScore >= 45) {
                resultText += '45 - 50: This is Good. You are generally still a positive thinker, sometimes. You could look for ways to improve a bit, if you are interested.';
            } else if (totalScore >= 40) {
                resultText += '40 - 45: This is Fair. Your positive side and your negative side are about evenly matched. Your call.';
            } else {
                resultText += 'Below 40: You tend to be pessimistic. Think of ways to improve your pessimistic approach to life, if you desire.';
            }

            document.getElementById('result-container').style.display = 'block';
            document.getElementById('result-text').innerHTML = resultText;
    }
